/**
 * @desc Read the standard input.
 *
 * @param  {Function} cb - `cb(err, readData)`
 */
function readUserInput(cb) {
	var chunk = '';
	process.stdin.on('readable', function () {
		var tmp = process.stdin.read();
		if (tmp !== null) {
			chunk += tmp;
		}
	});
	process.stdin.on('end', function () {
		return cb(null, chunk);
	});
}

